package zkxy.vo;

public class DefaultPsdChangeVO {
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getDefaultPsd() {
		return defaultPsd;
	}
	public void setDefaultPsd(String defaultPsd) {
		this.defaultPsd = defaultPsd;
	}
	public String getNewPsd() {
		return newPsd;
	}
	public void setNewPsd(String newPsd) {
		this.newPsd = newPsd;
	}
	public String getNewPsd2() {
		return newPsd2;
	}
	public void setNewPsd2(String newPsd2) {
		this.newPsd2 = newPsd2;
	}
	private String userName;
	private String defaultPsd;
	private String newPsd;
	private String newPsd2;

}
